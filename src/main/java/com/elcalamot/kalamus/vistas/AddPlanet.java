    private void a単adirplanetaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_a単adirplanetaMouseClicked
        if (evt.getButton() == BUTTON1) {

            String nombre = this.nombre1.getText();
            String galaxia = this.galaxia1.getText();
            String habitantes = this.habitants1.getValue().toString();
            String clima = this.clima1.getSelectedItem().toString();
            boolean flora = this.flora1.isSelected();
            boolean pez = this.pez1.isSelected();
            String dato1 = "no";
            String dato2 = "no";
            if (flora == true) {
                dato1 = "yes";
            } else if (flora == false) {
                dato1 = "no";
            }
            if (pez == true) {
                dato2 = "yes";
            } else if (pez == false) {
                dato2 = "no";
            }
            try {
                String[] datos = {"", "", nombre, galaxia, habitantes, clima, dato1, dato2};
                String out = "ERROR: Campo vacio o sin el valor esperado.";
                if (!nombre.isBlank() && !galaxia.isBlank() && !habitantes.isBlank()) {
                    out = FuncionesModelo_Planetas.crearPlaneta(datos, "jframe");
                }

                this.mensajepanel.append(out);
                this.mensaje.setVisible(true);
                
            } catch (IOException | ClassNotFoundException | SQLException e) {
                System.out.println(e.getMessage());
            }

            this.nombre1.setText(null);
            this.galaxia1.setText(null);
            this.flora1.setSelected(false);
            this.pez1.setSelected(false);

        }


    }//GEN-LAST:event_a単adirplanetaMouseClicked

